<div id="Historique" class="container text-center">
  <h2>Historique de l'Intelligence Artificielle</h2>
  <p>De tout temps, les humains ont créé des technologies pour se soulager des 
tâches quotidiennes. Le point d’évolution de l’humanité se produit lorsque 
l’humain maîtrise le feu : les aliments cuits sont plus faciles à digérer. Il y a 
besoin de moins de neurones dans notre cerveau bas, celui qui gère la digestion, 
et donc l’énergie disponible permet d’accroître notre cerveau haut. Ainsi l’être humain se trouve en haut de la chaîne alimentaire et devient libre de créer. 
Pour rester en haut de cette chaîne alimentaire, l’humain construit des outils 
qui ont un impact sur le développement de l’humanité. « La main forge l’outil 
et l’outil change l’homme », résumait ainsi l’ethnologue et historien André 
Leroi-Gourhan.</p>
  <br>
   
    <h3><em>L’histoire ancienne</em></h3>
	<p>L’humain a bien sûr fabriqué des outils pour multiplier la puissance de la 
force humaine. Dans l’Antiquité, les ingénieuses machines d’Archimède 
surpassaient tout ce que le muscle humain pouvait accomplir et, en l'an 150 
de notre ère, à Alexandrie, une machine à vapeur ouvrait seule la porte d’un 
temple. Par ailleurs, l’humain a aussi très tôt développé des outils pour se 
soulager des tâches intellectuelles. Ainsi, la machine d’Anticythère, deux siècles 
environ avant Jésus-Christ 7
, permettait de faire des calculs astronomiques 
sophistiqués, incluant des prédictions d’éclipses, tandis qu’au premier siècle 
de notre ère Héron d’Alexandrie inventait la première « machine à sous », 
qui distribuait une quantité précise d’eau bénite lorsqu’on y introduisait une 
pièce et fonctionnait grâce à une mécanique mue par l’eau selon le principe 
des boucles de rétroaction. Ces exemples montrent qu’il y a plus de deux mille 
ans, les principes de l’automatisme et de la systémique, qui sont les bases de 
l’informatique et de l’intelligence artificielle, étaient non seulement posés, mais 
donnaient lieu à la fabrication de leurs objets.
Les premiers ordinateurs n’étaient pas si éloignés des machines de l’Antiquité. 
La grande différence est la capacité de réaliser des opérations logiques 
intégrant le « et » et le « ou », grâce aux transistors. Ensuite, la miniaturisation 
a permis d’augmenter le nombre de composants élémentaires par surface, 
donc de passer à des calculs plus complexes. Si la technologie change, le désir 
de reproduire, voire de surpasser le raisonnement humain reste une constante 
dans l’histoire de l’humanité</p>

<h3><em>Des années 1950 à aujourd’hui</em></h3>
 <p>En 1952, Alan Turing, l’un des fondateurs de l’informatique, avait déjà réalisé 
un programme de jeu d’échecs, proche des systèmes experts actuels. Il fallait 
une demi-heure pour que la machine propose chaque coup. En 1956, le 
séminaire de Darmouth a réuni pendant dix semaines des scientifiques de 
très haut rang pour définir l’intelligence artificielle. Parmi eux se trouvaient 
John McCarthy, Marvin Minsky (cofondateur du laboratoire d’intelligence 
artificielle au MIT, père des réseaux de neurones), Herbert Simon (prix Nobel 
d’économie 1978, inventeur de la rationalité limitée) et Claude Shannon 
(inventeur de la théorie de l’information) 8
. L’un des objectifs fondamentaux de 
ce séminaire était de comprendre ce qu’était l’intelligence, au travers d’outils 
informatiques, avec un autre langage que celui des êtres humains. Il fut la base 
de nombreux travaux théoriques et pratiques, comme l’invention de langages 
de programmation orientés vers l’intelligence artificielle.
Cette compréhension de l’intelligence que souhaitaient appréhender les 
participants du séminaire se retrouve dans les mots de Hiroshi Ishiguro (voir 
photo), l’un des grands chercheurs sur les robots humanoïdes à l’université 
d’Osaka : « Je construis des robots pour comprendre ce qu’est la conscience 9
. » 
D’ailleurs, il est encore plus difficile de définir la conscience que l’intelligence, 
d’où l’idée intéressante de construire des machines encore plus perfectionnées 
que les ordinateurs pour interroger la conscience</p>

