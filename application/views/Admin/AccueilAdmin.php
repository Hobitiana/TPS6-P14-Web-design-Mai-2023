  <h2>Intelligence Artificielle</h2>
  <h3><em>. Qu’est-ce que l’intelligence?</em></h3>
  <p>Le mot « intelligence » est dérivé du latin intellegere ou intelligere (« choisir 
		entre ») 6
		. Notons qu’Internet, en amplifiant les mécanismes de renforcement 
		d’informations entre les êtres humains, a fortement contribué à installer le 
		concept d’intelligence collective. L’intelligence est la capacité dynamique de
		pouvoir faire des inférences entre des stimuli, d’en déduire des abstractions, 
		de créer un langage qui permet de nommer, d’échanger et de faire des liens 
		entre ces abstractions. L’intelligence a permis de définir le concept de contexte, 
		pouvant ainsi expliciter que les liens ne sont pas forcément répétitifs. C’est 
		dans toutes ces capacités que l’humain se distingue des autres mammifères. 
		Non seulement un chien ne sait pas dire « demain », mais il est probable que 
		le concept de « demain » ne soit pas développé dans ses capacités cognitives.
		Nous avons maintenant des outils pour comprendre le cerveau et nous savons 
		que le développement du cortex cérébral a autorisé l’homme à définir des 
		abstractions. Les fonctions du langage, consubstantielles à l’intelligence, ont 
		été identifiées dans certaines aires du cerveau, les aires de Broca et de Wernicke. 
		Certains animaux possèdent ces aires, mais elles sont fortement développées 
		chez les humains. Il faut noter que ce développement du cerveau explique 
		un phénomène intéressant : la plupart des mammifères naissent quasiment 
		adultes, sauf les humains. Un poulain sait marcher au bout de quelques heures, 
		par exemple. En revanche, c’est parce que le développement du cerveau ne 
		peut se faire in utero (car il serait trop volumineux pour franchir le col de 
		l’utérus) que les humains naissent « trop tôt ». Ce faisant, ils sont soumis très 
		vite à tous les stimuli d’un environnement plus varié que le ventre de la mère. 
		L’impact sur le développement de l’intelligence sera peut-être encore mieux 
		compris avec la découverte du rôle fondamental de l’épigénétique, science 
		étudiant les relations entre les gènes et l’environnement.
		Il n’est donc pas facile de définir l’intelligence avec le langage comme seul outil. 
		Un dictionnaire présente la particularité d’être autoréférent, puisque les mots 
		se définissent à l’aide de mots. En revanche, il faut comprendre l’intelligence 
		artificielle comme une tentative de comprendre l’intelligence en utilisant des 
		programmes informatiques. </p>
